package com.hyh.cloud.sso.client.configure;



import org.springframework.boot.context.properties.ConfigurationProperties;
import org.springframework.stereotype.Component;


@Component
@ConfigurationProperties(prefix="cookie")
public class CookieConfigure {
	
 private String domain;
    
 private String userTokenName;

 private String loginTimeOut;
 private String userTokenKeyCacheName;
public String getDomain() {
	return domain;
}
public void setDomain(String domain) {
	this.domain = domain;
}
public String getUserTokenName() {
	return userTokenName;
}
public void setUserTokenName(String userTokenName) {
	this.userTokenName = userTokenName;
}
public String getLoginTimeOut() {
	return loginTimeOut;
}
public void setLoginTimeOut(String loginTimeOut) {
	this.loginTimeOut = loginTimeOut;
}
public String getUserTokenKeyCacheName() {
	return userTokenKeyCacheName;
}
public void setUserTokenKeyCacheName(String userTokenKeyCacheName) {
	this.userTokenKeyCacheName = userTokenKeyCacheName;
}


}