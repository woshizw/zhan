package com.zhan.cloud.commons.base.constant;

/**
 * ucs枚举型常量
 * @author zhangw
 *
 */
public enum UcsEnumConstant {

	    userName("01","用户名"), password("02","密码");
	     
	     
	    private String name ;
	    private String code ;
	      
	    private UcsEnumConstant(String code, String name ){
	        this.name = name ;
	        this.code = code ;
	    }
	      
	    public String getName() {
	        return name;
	    }
	    public void setName(String name) {
	        this.name = name;
	    }
	    public String getCode() {
	        return code;
	    }
	    public void setCode(String code) {
	        this.code = code;
	    }
	
}
